import { useState, useEffect } from 'react'
import { useNavigate } from 'react-router-dom'
import HealthGauge from '../components/HealthGauge'
import SummaryCard from '../components/SummaryCard'

export default function Results() {
  const navigate = useNavigate()
  const [report, setReport] = useState(null)

  useEffect(() => {
    const raw = sessionStorage.getItem('labelx_last_report')
    if (raw) {
      try { 
        const parsed = JSON.parse(raw)
        
        // Backward compatibility for old cached reports
        if (parsed.summary && parsed.summary.unknown_count !== undefined) {
            parsed.summary.other_count = parsed.summary.unknown_count
        }
        if (parsed.ingredients) {
            parsed.ingredients = parsed.ingredients.map(i => ({
                ...i,
                safety_rating: i.safety_rating === 'unknown' ? 'other' : i.safety_rating
            }))
        }
        
        setReport(parsed) 
      }
      catch { navigate('/analyze') }
    } else {
      navigate('/analyze')
    }
  }, [])

  if (!report) return (
    <div className="min-h-screen flex items-center justify-center">
      <div className="text-slate-500">Loading results…</div>
    </div>
  )

  const summary = report.summary || {}
  const ingredients = report.ingredients || []
  const score = summary.health_score ?? 0

  const SEVERITY_ORDER = { harmful: 0, caution: 1, other: 2, safe: 3 }
  const sorted = [...ingredients].sort((a, b) =>
    (SEVERITY_ORDER[a.safety_rating] ?? 4) - (SEVERITY_ORDER[b.safety_rating] ?? 4)
  )

  const handleDownload = () => {
    const content = JSON.stringify(report, null, 2)
    const blob = new Blob([content], { type: 'application/json' })
    const url = URL.createObjectURL(blob)
    const a = document.createElement('a')
    a.href = url
    a.download = `labelx-report-${Date.now()}.json`
    a.click()
    URL.revokeObjectURL(url)
  }

  return (
    <div className="min-h-screen py-12 px-4 page-enter bg-[#f8fafc]">
      <div className="max-w-6xl mx-auto space-y-10">

        {/* Top Header Row */}
        <div className="flex items-center justify-between border-b border-slate-200/60 pb-6 bg-white p-6 rounded-t-2xl shadow-sm -mb-6 relative z-10">
          <div>
            <h1 className="font-display font-bold text-3xl text-slate-900">
              Safety Analysis Report
            </h1>
            <div className="flex items-center gap-3 mt-2">
              <p className="text-slate-500">Generated by LabelX multi-agent workflow</p>
              
              {summary.product_veg_status && (
                <span className={`px-2.5 py-0.5 rounded-full text-xs font-semibold uppercase tracking-wide border
                  ${summary.product_veg_status === 'vegan' ? 'bg-emerald-50 text-emerald-700 border-emerald-200' : 
                    summary.product_veg_status === 'veg' ? 'bg-green-50 text-green-700 border-green-200' : 
                    'bg-rose-50 text-rose-700 border-rose-200'}`}>
                  {summary.product_veg_status === 'non-veg' ? 'Non-Veg' : summary.product_veg_status}
                </span>
              )}
              
              {summary.processing_level && summary.processing_level !== 'unknown' && (
                <span className={`px-2.5 py-0.5 rounded-full text-xs font-semibold uppercase tracking-wide border
                  ${summary.processing_level === 'raw' ? 'bg-emerald-50 text-emerald-700 border-emerald-200' : 
                    summary.processing_level === 'minimally_processed' ? 'bg-green-50 text-green-700 border-green-200' : 
                    summary.processing_level === 'processed' ? 'bg-amber-50 text-amber-700 border-amber-200' : 
                    'bg-rose-50 text-rose-700 border-rose-200'}`}>
                  {summary.processing_level.replace('_', ' ')}
                </span>
              )}
            </div>
          </div>
          <div className="flex gap-2">
            <button onClick={() => navigate('/analyze')} className="btn-outline">
              New Analysis
            </button>
            <button onClick={handleDownload} className="btn-outline">
              JSON
            </button>
          </div>
        </div>

        <div className="bg-white rounded-b-2xl p-6 shadow-sm border border-t-0 border-slate-100 space-y-10 relative z-0">
          
          {/* Introductory Text */}
          <div className="text-slate-700 leading-relaxed max-w-4xl text-sm sm:text-base pt-4">
            <p>
              Hello! As a safety analyst, I have reviewed your list.
            </p>
            {(summary.personalized_summary || report.disclaimer || summary.top_warnings?.length > 0) && (
              <p className="mt-3">
                <strong className="font-semibold text-slate-900">Important Note:</strong>{' '}
                {summary.personalized_summary}
                {report.disclaimer && ` ${report.disclaimer}`}
                {summary.top_warnings?.length > 0 && ` Critical warnings: ${summary.top_warnings.join(', ')}.`}
                {summary.allergen_alerts?.length > 0 && ` Allergen alerts: ${summary.allergen_alerts.join(', ')}.`}
              </p>
            )}
          </div>

          {/* Stats Row */}
          <div className="grid grid-cols-2 md:grid-cols-5 gap-4">
            <div className="md:col-span-1 flex flex-col justify-center items-center bg-[#f8f9fc] rounded-xl border border-slate-100 shadow-sm p-4">
              <HealthGauge score={score} animated />
            </div>
            <SummaryCard title="Safe Ingredients" value={summary.safe_count ?? 0} color="green" />
            <SummaryCard title="Caution Items" value={summary.caution_count ?? 0} color="yellow" />
            <SummaryCard title="Harmful Items" value={summary.harmful_count ?? 0} color="red" />
            <SummaryCard title="Other Items" value={summary.other_count ?? 0} color="white" />
          </div>

          {/* Table Section */}
          <div>
            <h2 className="font-display font-bold text-xl text-[#1e293b] mb-4">
              Ingredient Analysis
            </h2>
            
            <div className="overflow-x-auto rounded-xl border border-slate-200 shadow-sm">
              <table className="w-full text-left border-collapse min-w-[800px]">
                <thead>
                  <tr className="bg-indigo-50/60 border-b border-slate-200">
                    <th className="py-4 px-6 font-semibold text-indigo-900 text-sm w-1/4">Ingredient</th>
                    <th className="py-4 px-6 font-semibold text-indigo-900 text-sm w-1/6">Safety Rating</th>
                    <th className="py-4 px-6 font-semibold text-indigo-900 text-sm w-1/6">Classification</th>
                    <th className="py-4 px-6 font-semibold text-indigo-900 text-sm">Concerns / Notes</th>
                  </tr>
                </thead>
                <tbody className="divide-y divide-slate-100 bg-white">
                  {sorted.map((ing, idx) => (
                    <tr key={idx} className="hover:bg-slate-50/50 transition-colors">
                      <td className="py-4 px-6 align-top">
                        <div className="font-medium text-slate-900">{ing.name}</div>
                        {ing.aliases?.length > 0 && (
                          <div className="text-xs text-slate-400 mt-1">{ing.aliases.join(', ')}</div>
                        )}
                      </td>
                      <td className="py-4 px-6 align-top">
                        <span className={`inline-flex px-0 py-1 rounded-md text-xs font-semibold uppercase tracking-wider
                          ${ing.safety_rating === 'safe' ? 'text-emerald-700' : 
                            ing.safety_rating === 'caution' ? 'text-amber-700' : 
                            ing.safety_rating === 'harmful' ? 'text-rose-700' : 
                            'text-slate-600'}`}>
                          {ing.safety_rating === 'other' ? 'OTHER' : 
                           ing.safety_rating === 'safe' ? 'SAFE' : 
                           ing.safety_rating === 'caution' ? 'CAUTION' : 
                           ing.safety_rating === 'harmful' ? 'HARMFUL' : ing.safety_rating}
                        </span>
                      </td>
                      <td className="py-4 px-6 align-top space-y-2">
                        {ing.processing_level && ing.processing_level !== 'unknown' && (
                           <div className="text-[10px] uppercase font-bold text-slate-500 tracking-wider">
                             {ing.processing_level.replace('_', ' ')}
                           </div>
                        )}
                        {ing.is_veg === false && (
                           <div className="text-[10px] uppercase font-bold text-rose-600 tracking-wider">
                             NON-VEG ({ing.ingredient_source})
                           </div>
                        )}
                        {ing.is_veg !== false && (!ing.processing_level || ing.processing_level === 'unknown') && (
                           <div className="text-[10px] uppercase text-slate-300">
                             —
                           </div>
                        )}
                      </td>
                      <td className="py-4 px-6 align-top">
                        <p className="text-sm text-slate-700">{ing.explanation || ing.health_impact || 'No specific concerns known.'}</p>
                        {ing.personalized_note && (
                          <p className="text-sm text-amber-700 mt-2 font-medium bg-amber-50 p-2 rounded-md border border-amber-100">
                            {ing.personalized_note}
                          </p>
                        )}
                      </td>
                    </tr>
                  ))}
                  {sorted.length === 0 && (
                    <tr>
                      <td colSpan={3} className="py-8 text-center text-slate-400">
                        No ingredients found.
                      </td>
                    </tr>
                  )}
                </tbody>
              </table>
            </div>
          </div>
        </div>

      </div>
    </div>
  )
}
